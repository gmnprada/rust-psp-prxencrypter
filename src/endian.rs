// Endian Function Inline 
#[inline(always)]
pub fn swap16(v: u16) -> u16 {
    ((v & 0x00FF) << 8) | ((v & 0xFF00) >> 8)
}

#[inline(always)]
pub fn swap32(v: u32) -> u32 {
    ((v & 0x000000FF) << 24) | ((v & 0x0000FF00) << 8) |
    ((v & 0xFF000000) >> 24) | ((v & 0x00FF0000) >> 8)
}

#[inline(always)]
pub fn swap64(v: u64) -> u64 {
    ((v & 0x00000000000000FF_u64) << 56) |
    ((v & 0x000000000000FF00_u64) << 40) |
    ((v & 0x0000000000FF0000_u64) << 24) |
    ((v & 0x00000000FF000000_u64) <<  8) |
    ((v & 0x000000FF00000000_u64) >>  8) |
    ((v & 0x0000FF0000000000_u64) >> 24) |
    ((v & 0x00FF000000000000_u64) >> 40) |
    ((v & 0xFF00000000000000_u64) >> 56)
}